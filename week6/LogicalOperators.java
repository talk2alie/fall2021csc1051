public class LogicalOperators {
    public static void main(String[] args) {
        /**
            &&  -   Logical AND
            E.g 5 > 4 && 2 < 10

            ||  -   Logical OR
            E.g 5 > 4 || 2 > 1


            !   -   Logical NOT
            E.g !(5 > 4)
                !true
                !false

            ^   -   Logical Exclusive OR 
            true ^ true     => false
            true ^ false    => true
            false ^ false   => false
         */
    }
}
